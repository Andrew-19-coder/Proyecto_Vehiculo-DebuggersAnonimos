    private void btnMotorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMotorActionPerformed
        if(motor.isEncendido()){
            motor.apagarMotor();
            btnMotor.setText("Encender Motor");
            btnMotor.setBackground(Color.GREEN);
            panelSistemas.setVisible(false);
        }else{
            motor.encenderMotor();
            btnMotor.setText("Apagar Motor");
            btnMotor.setBackground(Color.RED);
            panelSistemas.setVisible(true);
        }
        
        CardLayout cl = (CardLayout) panelContenedor.getLayout();
        cl.show(panelContenedor, "ENCENDIDO");
        
    }//GEN-LAST:event_btnMotorActionPerformed

